#!/usr/bin/env bash
# monitors.sh — monitor wizard + profiles (Hyprland + Waybar). Subcommands: list | setup | apply

set -uo pipefail

CFG="${XDG_CONFIG_HOME:-$HOME/.config}"
HYPR_DIR="$CFG/hypr"
WAYBAR_DIR="$CFG/waybar"
PROFILES="$HYPR_DIR/monitor-profiles.json"
ACTIVE_LUA="$HYPR_DIR/monitors_active.lua"
WAYBAR_CFG="$WAYBAR_DIR/config"
BARS_TEMPLATE="$WAYBAR_DIR/bars.json"

msg()  { printf '\033[1;34m󰍹 monitors:\033[0m %s\n' "$*"; }
warn() { printf '\033[1;33m󰍹 monitors:\033[0m %s\n' "$*" >&2; }
notify() { command -v notify-send >/dev/null 2>&1 && notify-send -a "Monitors" "Monitors" "$*" || true; }

need() {
    command -v "$1" >/dev/null 2>&1 || { warn "missing '$1' (required)"; exit 1; }
}

detect_json() { hyprctl monitors all -j; }

clean_mode() { sed -E 's/Hz$//; s/@([0-9]+)\.[0-9]+$/@\1/'; }

cmd_list() {
    need jq
    {
        printf 'IDX|DESCRIPTION (identifier)|PORT|MODE\n'
        detect_json | jq -r '
            to_entries[] |
            "\(.key)|\(.value.description)|\(.value.name)|\(.value.availableModes[0] // "?")"
        '
    } | column -t -s '|'
    printf '\nUse the DESCRIPTION column verbatim in Waybar / profiles.\n'
}

default_profile() {
    detect_json | jq '
        ([ .[] | (.width*.height) ] | max) as $maxarea
        | ([ .[] | (.width*.height) ] | index($maxarea)) as $pidx
        | [ to_entries[]
            | .key as $k | .value
            | {
                description: .description,
                mode: ((.availableModes[0] // "\(.width)x\(.height)@60Hz") | sub("Hz$";"")),
                scale: .scale,
                transform: 0,
                primary: ($k == $pidx),
                name: .name
              }
          ]
        | map(.bar = (if (.primary or (.name|startswith("eDP"))) then "full" else "minimal" end))
        | map(del(.name))
    '
}

sig_of_descriptions() { jq -r '[.[].description] | sort | join("|")'; }

port_for_desc() {
    jq -r --arg d "$1" '.[] | select(.description == $d) | .name' | head -n1
}

generate() {
    local out_lua="$1" out_waybar="$2"
    local profile detected
    profile="$(cat)"
    detected="$(detect_json)"

    {
        printf -- '-- monitors_active.lua — GENERATED by monitors.sh. Do not edit by hand.\n'
        printf -- '-- Run `monitors.sh setup` to change the layout.\n\n'

        local x=0 pair=3
        local n; n="$(jq 'length' <<<"$profile")"
        local ws_lines=""
        local i
        for ((i=0; i<n; i++)); do
            local desc mode scale transform primary bar port w h logical portrait
            desc="$(jq -r ".[$i].description" <<<"$profile")"
            mode="$(jq -r ".[$i].mode" <<<"$profile")"
            scale="$(jq -r ".[$i].scale" <<<"$profile")"
            transform="$(jq -r ".[$i] | (.transform // (if .orientation == \"v\" then 3 else 0 end))" <<<"$profile")"
            primary="$(jq -r ".[$i].primary" <<<"$profile")"
            port="$(printf '%s' "$detected" | port_for_desc "$desc")"
            [ -z "$port" ] && continue

            w="${mode%x*}"
            h="${mode#*x}"; h="${h%@*}"
            # transforms 1/3/5/7 rotate 90°/270° -> swap width/height (portrait)
            if [ $((transform % 2)) -eq 1 ]; then portrait=1; else portrait=0; fi
            if [ "$portrait" = "1" ]; then
                logical="$(awk -v a="$h" -v s="$scale" 'BEGIN{printf "%d", a/s}')"
            else
                logical="$(awk -v a="$w" -v s="$scale" 'BEGIN{printf "%d", a/s}')"
            fi

            printf -- 'hl.monitor({ output = "%s", mode = "%s", position = "%dx0", scale = %s, transform = %d })\n' \
                "$port" "$mode" "$x" "$scale" "$transform"

            local layoutopt=""
            [ "$portrait" = "1" ] && layoutopt=', layout_opts = { orientation = "top" }'
            if [ "$primary" = "true" ]; then
                ws_lines+=$(printf 'hl.workspace_rule({ workspace = "1", monitor = "%s"%s })' "$port" "$layoutopt")$'\n'
                ws_lines+=$(printf 'hl.workspace_rule({ workspace = "2", monitor = "%s"%s })' "$port" "$layoutopt")$'\n'
            else
                ws_lines+=$(printf 'hl.workspace_rule({ workspace = "%d", monitor = "%s"%s })' "$pair" "$port" "$layoutopt")$'\n'
                ws_lines+=$(printf 'hl.workspace_rule({ workspace = "%d", monitor = "%s"%s })' "$((pair+1))" "$port" "$layoutopt")$'\n'
                pair=$((pair+2))
            fi

            x=$((x + logical))
        done

        printf -- '\n'
        printf -- '%s\n' "$ws_lines"
    } > "$out_lua"

    if [ ! -f "$BARS_TEMPLATE" ]; then
        warn "$BARS_TEMPLATE not found; skipping waybar regeneration"
        return 0
    fi

    local bars='[]'
    local n; n="$(jq 'length' <<<"$profile")"
    local i
    for ((i=0; i<n; i++)); do
        local desc bar port logical mode transform scale w h archetype fw portrait
        desc="$(jq -r ".[$i].description" <<<"$profile")"
        bar="$(jq -r ".[$i].bar" <<<"$profile")"
        [ "$bar" = "none" ] && continue
        port="$(printf '%s' "$detected" | port_for_desc "$desc")"
        [ -z "$port" ] && continue
        archetype="$(jq -c --arg t "$bar" '.[$t] // empty' "$BARS_TEMPLATE")"
        [ -z "$archetype" ] && { warn "bar type '$bar' not in bars.json; skipping"; continue; }
        mode="$(jq -r ".[$i].mode" <<<"$profile")"
        transform="$(jq -r ".[$i] | (.transform // (if .orientation == \"v\" then 3 else 0 end))" <<<"$profile")"
        scale="$(jq -r ".[$i].scale" <<<"$profile")"
        w="${mode%x*}"; h="${mode#*x}"; h="${h%@*}"
        if [ $((transform % 2)) -eq 1 ]; then portrait=1; else portrait=0; fi
        if [ "$portrait" = "1" ]; then
            logical="$(awk -v a="$h" -v s="$scale" 'BEGIN{printf "%d", a/s}')"
        else
            logical="$(awk -v a="$w" -v s="$scale" 'BEGIN{printf "%d", a/s}')"
        fi
        fw=$((logical - 120)); [ "$fw" -lt 400 ] && fw=$logical

        local barjson
        barjson="$(jq -c --arg out "$port" --argjson w "$fw" \
            'del(._comment) | .output = [$out] | (if has("width") then . else .width = $w end)' \
            <<<"$archetype")"
        bars="$(jq --argjson b "$barjson" '. + [$b]' <<<"$bars")"
    done
    printf '%s\n' "$bars" | jq '.' > "$out_waybar"
}

cmd_apply() {
    need jq
    local detected sig profile used_default=0
    detected="$(detect_json)"
    sig="$(printf '%s' "$detected" | sig_of_descriptions)"

    if [ -f "$PROFILES" ] && jq -e --arg s "$sig" 'has($s)' "$PROFILES" >/dev/null 2>&1; then
        profile="$(jq -c --arg s "$sig" '.[$s]' "$PROFILES")"
        msg "profile found for this monitor set"
    else
        profile="$(default_profile)"
        used_default=1
        msg "no profile for this set → using default (horizontal, primary=largest)"
    fi

    local tmp_lua tmp_wb
    tmp_lua="$(mktemp)"; tmp_wb="$(mktemp)"
    printf '%s' "$profile" | generate "$tmp_lua" "$tmp_wb"

    mkdir -p "$HYPR_DIR" "$WAYBAR_DIR"

    # Reload only if the lua changed: a hotplug hl.on handler calls apply, so
    # guarding the reload prevents an event -> reload -> event loop.
    local lua_changed=0 wb_changed=0
    if ! cmp -s "$tmp_lua" "$ACTIVE_LUA" 2>/dev/null; then lua_changed=1; fi
    if ! cmp -s "$tmp_wb" "$WAYBAR_CFG" 2>/dev/null; then wb_changed=1; fi

    if [ "$lua_changed" -eq 1 ]; then
        mv "$tmp_lua" "$ACTIVE_LUA"
        msg "layout changed; reloading Hyprland…"
        hyprctl reload >/dev/null 2>&1 || true
    else
        rm -f "$tmp_lua"
    fi

    if [ "$wb_changed" -eq 1 ]; then
        mv "$tmp_wb" "$WAYBAR_CFG"
    else
        rm -f "$tmp_wb"
    fi

    if [ "$wb_changed" -eq 1 ] || ! pgrep -x waybar >/dev/null 2>&1; then
        killall waybar >/dev/null 2>&1 || true
        setsid waybar >/dev/null 2>&1 < /dev/null &
        msg "Waybar (re)started"
    fi

    [ "$lua_changed" -eq 0 ] && [ "$wb_changed" -eq 0 ] && msg "no changes" || true
    [ "$used_default" -eq 1 ] && notify "Using default layout. Run 'monitors.sh setup' to customize." || true
}

ask() {
    local p="$1" d="${2:-}" a
    read -r -p "$p" a < /dev/tty || a=""
    printf '%s' "${a:-$d}"
}

cmd_setup() {
    need jq
    exec < /dev/tty
    local detected n
    detected="$(detect_json)"
    n="$(jq 'length' <<<"$detected")"
    [ "$n" -eq 0 ] && { warn "no monitors detected"; exit 1; }

    msg "Detected $n monitor(s):"
    cmd_list
    printf '\n'

    local entries='[]'
    local i
    for ((i=0; i<n; i++)); do
        local desc port mode scale en trans bar
        desc="$(jq -r ".[$i].description" <<<"$detected")"
        port="$(jq -r ".[$i].name" <<<"$detected")"
        mode="$(jq -r ".[$i] | (.availableModes[0] // \"\(.width)x\(.height)@60Hz\")" <<<"$detected" | clean_mode)"
        scale="$(jq -r ".[$i].scale" <<<"$detected")"

        printf '\n\033[1m── %s (%s) ──\033[0m\n' "$desc" "$port"
        en="$(ask "  Enable this monitor? [Y/n]: " "y")"
        case "$en" in [nN]*) printf '  → disabled\n'; continue ;; esac

        printf '    Rotation: 0=normal  1=90°(portrait)  2=180°(flipped)  3=270°(portrait)\n'
        printf '              4-7 = mirrored (4=normal, 5=90°, 6=180°, 7=270°)\n'
        trans="$(ask "  Transform [0-7] (default 0): " "0")"
        case "$trans" in ''|*[!0-7]*) trans=0 ;; esac

        scale="$(ask "  Scale (default $scale): " "$scale")"
        bar="$(ask "  Waybar bar: [f]ull / [m]inimal / [n]one (default f): " "f")"
        case "$bar" in [mM]*) bar="minimal" ;; [nN]*) bar="none" ;; *) bar="full" ;; esac

        entries="$(jq \
            --arg desc "$desc" --arg mode "$mode" --argjson scale "$scale" \
            --argjson trans "$trans" --arg bar "$bar" \
            '. + [{description:$desc, mode:$mode, scale:$scale, transform:$trans, primary:false, bar:$bar}]' \
            <<<"$entries")"
    done

    local cnt; cnt="$(jq 'length' <<<"$entries")"
    [ "$cnt" -eq 0 ] && { warn "no monitors enabled"; exit 1; }

    printf '\n\033[1mOrder left → right\033[0m (enabled monitors):\n'
    local j
    for ((j=0; j<cnt; j++)); do printf '  %d) %s\n' "$j" "$(jq -r ".[$j].description" <<<"$entries")"; done
    local ord; ord="$(ask "  Indices separated by space (default 0..$((cnt-1))): " "")"
    if [ -n "$ord" ]; then
        entries="$(jq -c -n --argjson e "$entries" --arg ord "$ord" '
            ($ord | split(" ") | map(tonumber)) as $idx | [ $idx[] | $e[.] ]')"
    fi

    printf '\n\033[1mPrimary monitor\033[0m (gets workspaces 1 and 2):\n'
    for ((j=0; j<cnt; j++)); do printf '  %d) %s\n' "$j" "$(jq -r ".[$j].description" <<<"$entries")"; done
    local prim; prim="$(ask "  Primary index (default 0): " "0")"
    entries="$(jq --argjson p "$prim" 'to_entries | map(.value.primary = (.key == $p)) | map(.value)' <<<"$entries")"

    local sig; sig="$(printf '%s' "$detected" | sig_of_descriptions)"
    mkdir -p "$HYPR_DIR"
    [ -f "$PROFILES" ] || echo '{}' > "$PROFILES"
    local merged; merged="$(jq --arg s "$sig" --argjson e "$entries" '.[$s] = $e' "$PROFILES")"
    printf '%s\n' "$merged" > "$PROFILES"
    msg "profile saved for: $sig"

    cmd_apply
}

case "${1:-apply}" in
    list)  cmd_list ;;
    setup) cmd_setup ;;
    apply) cmd_apply ;;
    *) warn "usage: monitors.sh [list|setup|apply]"; exit 1 ;;
esac
